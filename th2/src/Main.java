public class Main {
    public static void main(String[] args) {
        int a = 10;
        float b = 20.5f;
        double c = 20.5;
        boolean d = true;
        char e = 'a';
        String j = "ha noi";

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("e = " + e);
        System.out.println("j = " + j);
    }
}
